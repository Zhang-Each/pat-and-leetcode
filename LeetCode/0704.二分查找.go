// @lc code=start
func search(nums []int, target int) int {
	var left int = 0
	var right int = len(nums) - 1
	for ; left <= right; {
		var mid int = (left + right) / 2
		if nums[mid] < target {
			left = mid + 1
		} else if nums[mid] > target {
			right = mid - 1
		} else {
			return mid
		}
	}
	return -1
}
